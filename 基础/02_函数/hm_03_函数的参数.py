# 开发一个 sum_2_num 的函数，实现两个数字的求和功能。


def sum_2_num():
    """基础版：对两个数字的求和"""

    num1 = int(input("请输入第一个数："))
    num2 = int(input("请输入第二个数："))
    result = num1 + num2
    print("%d + %d = %d" % (num1, num2, result))


sum_2_num()


"""
关于函数参数的使用：
①函数名（）内填写的是参数，参数之间用“，”隔开
②调用函数时（）内的叫实参，用来把数据传递到函数内部的[如下方的（1，2）]
③定义函数时（）内的叫形参，用来接收参数或者作为函数的变量使用[如下方的（x1, x2, result）]
"""


def y(x1, x2):
    """进阶版：调用参数"""

    resulty = x1 + x2
    print("%d + %d = %d" % (x1, x2, resulty))


y(1, 2)
